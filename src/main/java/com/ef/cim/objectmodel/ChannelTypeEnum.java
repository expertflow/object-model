package com.ef.cim.objectmodel;

/**
 * A {@code ChannelTypeEnum} enum represents all the possible modes a ChannelType can have. A ChannelType can
 * have one of the these modes:
 * WHATSAPP,
 * FACEBOOK,
 * VIBER,
 * SMS,
 * WEB,
 * GENERIC
 */
public enum ChannelTypeEnum {
    WHATSAPP,
    FACEBOOK,
    VIBER,
    SMS,
    WEB,
    GENERIC
}
